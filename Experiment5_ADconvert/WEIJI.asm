DATAS SEGMENT
    X DB 7BH
DATAS ENDS
STACKS SEGMENT
    DB 100 DUP(?)
STACKS ENDS
CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
MAIN PROC FAR
    MOV AX,DATAS
    MOV DS,AX
    MOV DL,X
    MOV CL,4
    SHR DL,CL
    CALL CHANGE
    MOV DL,X
    AND DL,0FH
    CALL CHANGE
    MOV AH,4CH
    INT 21H
MAIN ENDP
CHANGE PROC
	CMP DL,9
	JBE NEXT
	ADD DL,7
NEXT:ADD DL,30H
	MOV AH,2
	INT 21H
CODES ENDS
	END MAIN
