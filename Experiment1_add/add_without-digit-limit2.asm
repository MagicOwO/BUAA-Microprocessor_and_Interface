DATAS SEGMENT
	X1 DB 100 DUP(?)
	X2 DB 100 DUP(?)
	X3 DB 101 DUP(?)
	COUNT1 DB 0
	COUNT2 DB 0
DATAS ENDS

STACKS SEGMENT
	DB 100 DUP('STACK')
STACKS ENDS

CODES SEGMENT
	ASSUME CS:CODES,DS:DATAS,SS:STACKS
MAIN PROC FAR
	MOV DL,' '
	MOV AH,2H
	INT 21H
	MOV AX,DATAS
	MOV DS,AX
	MOV SI,0
INPUT1:CALL DAYIN;INPUT DATA
	AND AL,0FH
	MOV [SI+X1],AL
	INC SI
	CMP AL,0DH
	JNZ INPUT1
	DEC SI
	AND CX,0
	MOV CX,SI
	MOV BX,OFFSET COUNT1
	MOV [BX],CL
	MOV DL,0AH;换行回车
    MOV AH,2H
    INT 21H
    MOV DL,0DH
    MOV AH,2H
    INT 21H
	MOV DL,'+'
	MOV AH,2H
	INT 21H
INPUT2:CALL DAYIN;INPUT DATA
	AND AL,0FH
	MOV [SI+X2],AL
	INC SI
	CMP AL,0DH
	JNZ INPUT2
	DEC SI
	AND CX,0
	MOV CX,SI
	MOV BX,OFFSET COUNT2
	MOV [BX],CL
	MOV DL,0AH;换行回车
    MOV AH,2H
    INT 21H
    MOV DL,0DH
    MOV AH,2H
    INT 21H
	
	;CALCULATE
	;OUTPUT DATA
	MOV AH,4CH
	INT 21H
MAIN ENDP

DAYIN PROC FAR;DAYIN DATA
AGAIN:MOV AH,8
	INT 21H
	CMP AL,0DH
	JZ EXIT
	CMP AL,30H
	JB AGAIN
	CMP AL,39H
	JA AGAIN
	MOV DL,AL
	MOV AH,2
	INT 21H
EXIT:RET
DAYIN ENDP
CODES ENDS
END MAIN













